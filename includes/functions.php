		$qry = mysql_query($sql);
		if(mysql_num_rows($qry) > 0){		
			while($row = mysql_fetch_array($qry))
			{
				$rid = $row['ID'];
				$user_nicename = $row['user_nicename'];
				$user_email = $row['user_email'];
				$user_type = $row['user_type'];
			}
			
			$_SESSION['restaurant_id'] = $rid;
			$_SESSION['restaurant_user_user_nicename'] = $user_nicename;
			$_SESSION['restaurant_user_email'] = $user_email;
			$_SESSION['restaurant_user_type']= $user_type;

		}
		return $rid;
	}
